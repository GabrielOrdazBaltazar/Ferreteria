    private void txtBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtBuscarActionPerformed
        int     id_histCompra        = Integer.parseInt(txtId.getText());
        paquete1.HistCompraSQL hcomp = new paquete1.HistCompraSQL();
        hcomp.buscarHistCompra(id_histCompra);
        paquete1.HistCompra hcomp1; 
        hcomp1 = hcomp.buscarHistCompra(id_histCompra);
        
        txtFecha.setText(hcomp1.getFecha());
        txtPrecioUnitario.setText(String.valueOf(hcomp1.getPrecio_unitario()));        
    }//GEN-LAST:event_txtBuscarActionPerformed

