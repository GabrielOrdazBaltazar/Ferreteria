    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        //Actualizar 
        int id_histcomp = Integer.parseInt(txtId.getText());
        String fecha = txtFecha.getText();
        double precio_unitario = Double.parseDouble(txtPrecioUnitario.getText());
        paquete1.HistCompraSQL hcomp = new  paquete1.HistCompraSQL();
        hcomp.actualizarHistCompra(id_histcomp, fecha, precio_unitario);
        //Limpiando
        txtId.setText("");
        txtFecha.setText("");
        txtPrecioUnitario.setText("");  
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int id_histcompra     = Integer.parseInt(txtId.getText());
        String fecha           = txtFecha.getText();
        double precio_unitario = Double.parseDouble(txtPrecioUnitario.getText());
        paquete1.HistCompraSQL hcomp = new paquete1.HistCompraSQL();
        hcomp.eliminarHistCompra(id_histcompra);
        //Limpiando
        txtId.setText("");
        txtFecha.setText("");
        txtPrecioUnitario.setText("");         
    }//GEN-LAST:event_btnEliminarActionPerformed

